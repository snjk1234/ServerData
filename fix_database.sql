UPDATE public.users
SET role = 'admin',
    is_active = TRUE,
    is_trusted = TRUE,
    allowed_categories = ARRAY['الكل', 'فلورينا', 'فرنشايز', 'جملة', 'موزع معتمد', 'اسكتشر', 'فيلانتو', 'الإدارة']
WHERE id = (
  SELECT id FROM auth.users WHERE email = 'your-email@example.com' LIMIT 1
);

