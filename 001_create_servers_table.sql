-- تقرير بعدد اليوزرات حسب الحالة
CREATE OR REPLACE VIEW تقرير_اليوزرات_حسب_الحالة AS
SELECT 
    حالة_اليوزر,
    COUNT(*) as عدد_اليوزرات,
    ROUND(COUNT(*) * 100.0 / SUM(COUNT(*)) OVER(), 2) as النسبة_المئوية
FROM بيانات_السيرفر
GROUP BY حالة_اليوزر
ORDER BY COUNT(*) DESC;

-- تقرير بالأفرع المنشأة خلال فترة محددة
CREATE OR REPLACE FUNCTION تقرير_الأفرع_حسب_الفترة(تاريخ_البداية TIMESTAMPTZ, تاريخ_النهاية TIMESTAMPTZ)
RETURNS TABLE (
    رقم_الفرع TEXT,
    اسم_الفرع_ar TEXT,
    اسم_الفرع_en TEXT,
    تاريخ_الانشاء TIMESTAMPTZ,
    حالة_اليوزر TEXT
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        s.رقم_الفرع,
        s.اسم_الفرع_ar,
        s.اسم_الفرع_en,
        s.تاريخ_الانشاء,
        s.حالة_اليوزر
    FROM بيانات_السيرفر s
    WHERE s.تاريخ_الانشاء BETWEEN تاريخ_البداية AND تاريخ_النهاية
    ORDER BY s.تاريخ_الانشاء DESC;
END;
$$ LANGUAGE plpgsql;

-- ============================================
-- إنشاء دالة لتصدير البيانات إلى CSV
-- ============================================

CREATE OR REPLACE FUNCTION تصدير_البيانات_إلى_CSV()
RETURNS TEXT AS $$
DECLARE
    result TEXT;
BEGIN
    SELECT string_agg(
        format(
            '%s,%s,%s,%s,%s,%s,%s',
            رقم_الفرع,
            اسم_الفرع_ar,
            اسم_الفرع_en,
            اسم_اليوزر,
            '***', -- الباسوورد لا يظهر في التصدير
            تاريخ_الانشاء,
            حالة_اليوزر
        ),
        E'
'
    )
    INTO result
    FROM بيانات_السيرفر;

    RETURN 'رقم_الفرع,اسم_الفرع_ar,اسم_الفرع_en,اسم_اليوزر,باسوورد,تاريخ_الانشاء,حالة_اليوزر
' || COALESCE(result, '');
END;
$$ LANGUAGE plpgsql;
