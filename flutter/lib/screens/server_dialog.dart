import 'package:flutter/material.dart';
import 'package:supabase_flutter/supabase_flutter.dart';
import 'package:aescryptojs/aescryptojs.dart';
import '../models/server_data.dart';

class ServerDialog extends StatefulWidget {
  final ServerData? serverToEdit;
  final List<ServerData> allServers;
  final VoidCallback onSaved;

  const ServerDialog({
    super.key,
    this.serverToEdit,
    required this.allServers,
    required this.onSaved,
  });

  @override
  State<ServerDialog> createState() => _ServerDialogState();
}

class _ServerDialogState extends State<ServerDialog> {
  static const adminKey = String.fromEnvironment('ADMIN_KEY', defaultValue: 'sols');

  late TextEditingController branchNoController;
  late TextEditingController nameArController;
  late TextEditingController nameEnController;
  late TextEditingController usernameController;
  late TextEditingController passwordController;
  late TextEditingController serialController;
  late TextEditingController streetController;
  late TextEditingController cityController;
  late TextEditingController taxNoController;
  late TextEditingController printerA4Controller;
  late TextEditingController printerBillController;

  String selectedCategory = 'فلورينا';
  String selectedStatus = 'يعمل';
  String? selectedRegion;
  bool branchNoEdited = false;
  bool obscurePassword = true;

  final List<String> _regions = [
    '1-المدينة المنورة',
    '1-المنطقة الوسطى',
    '2-الرياض',
    '2-الدمام',
    '3-الجنوبية - أبها',
    '4-جدة',
    '4-الغربية - مكة',
    '5-الشمالية تبوك',
    '5-الطائف'
  ];

  @override
  void initState() {
    super.initState();
    final s = widget.serverToEdit;
    branchNoController = TextEditingController(text: s?.branchNo ?? '');
    nameArController = TextEditingController(text: s?.branchNameAr ?? '');
    nameEnController = TextEditingController(text: s?.branchNameEn ?? '');
    usernameController = TextEditingController(text: s?.username ?? '');
    passwordController = TextEditingController();
    serialController = TextEditingController(text: s?.serialNumber?.toString() ?? '');
    streetController = TextEditingController(text: s?.streetName ?? '');
    cityController = TextEditingController(text: s?.cityName ?? '');
    taxNoController = TextEditingController(text: s?.taxNo ?? '');
    printerA4Controller = TextEditingController(text: s?.printerA4 ?? '');
    printerBillController = TextEditingController(text: s?.printerBill ?? '');

    selectedCategory = s?.category ?? 'فلورينا';
    selectedStatus = s?.status ?? 'يعمل';
    selectedRegion = s?.region;
    
    if (selectedRegion != null && !_regions.contains(selectedRegion)) {
      selectedRegion = null;
    }

    branchNoEdited = s != null;

    if (s == null) {
      updateAutoGeneratedFields(selectedCategory);
    }

    nameEnController.addListener(() {
      final text = nameEnController.text.toUpperCase();
      if (text.length >= 4 && selectedCategory != 'الإدارة') {
        printerA4Controller.text = '${text.substring(0, 4)}BIGPRIN1';
        printerBillController.text = '${text.substring(0, 4)}SPRI';
      }
    });
  }

  @override
  void dispose() {
    branchNoController.dispose();
    nameArController.dispose();
    nameEnController.dispose();
    usernameController.dispose();
    passwordController.dispose();
    serialController.dispose();
    streetController.dispose();
    cityController.dispose();
    taxNoController.dispose();
    printerA4Controller.dispose();
    printerBillController.dispose();
    super.dispose();
  }

  Map<String, bool> _getRequiredFields(String category) {
    if (category == 'فرنشايز' || category == 'فيلانتو') {
      return {'street': true, 'city': true, 'taxNo': true};
    } else if (category == 'فلورينا' || category == 'جملة') {
      return {'street': false, 'city': true, 'taxNo': false};
    } else {
      return {'street': false, 'city': false, 'taxNo': false};
    }
  }

  void updateAutoGeneratedFields(String category) {
    if (branchNoEdited && widget.serverToEdit != null) return; 
    if (branchNoEdited) return; 

    int startNum = 1;
    if (category == 'اسكتشر') startNum = 300;
    else if (category == 'فرنشايز') startNum = 500;
    else if (category == 'موزع معتمد') startNum = 600;
    else if (category == 'فيلانتو') startNum = 900;
    else if (category == 'الإدارة') startNum = 1;

    final categoryServers = widget.allServers.where((s) {
      if (category == 'فلورينا' || category == 'جملة') {
        return s.category == 'فلورينا' || s.category == 'جملة';
      }
      return s.category == category;
    }).toList();

    int nextNum = startNum;
    if (categoryServers.isNotEmpty) {
      int maxNum = 0;
      for (var s in categoryServers) {
        final numStr = s.branchNo.replaceAll(RegExp(r'\D'), '');
        final n = int.tryParse(numStr);
        if (n != null && n > maxNum) maxNum = n;
      }
      if (maxNum >= startNum) {
        nextNum = maxNum + 1;
      }
    }

    final hasBR = widget.allServers.any((s) => s.branchNo.startsWith('BR'));
    String formattedNo = category == 'الإدارة' 
        ? 'h-$nextNum' 
        : (hasBR ? 'BR${nextNum.toString().padLeft(3, '0')}' : nextNum.toString());

    branchNoController.text = formattedNo;

    int nextSerial = 100000;
    if (widget.allServers.isNotEmpty) {
      int maxSerial = 0;
      for (var s in widget.allServers) {
        if (s.serialNumber != null && s.serialNumber! > maxSerial) {
          maxSerial = s.serialNumber!;
        }
      }
      nextSerial = maxSerial >= 100000 ? maxSerial + 100000 : 100000;
    }
    serialController.text = nextSerial.toString();
  }

  InputDecoration _customDecoration({required String label, IconData? icon, Widget? suffix}) {
    return InputDecoration(
      labelText: label,
      labelStyle: const TextStyle(color: Colors.indigo, fontSize: 14),
      prefixIcon: icon != null ? Icon(icon, color: Colors.indigo) : null,
      suffixIcon: suffix,
      filled: true,
      fillColor: Colors.indigo.withOpacity(0.03),
      border: OutlineInputBorder(borderRadius: BorderRadius.circular(15), borderSide: BorderSide.none),
      enabledBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(15), borderSide: BorderSide(color: Colors.indigo.withOpacity(0.1))),
      focusedBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(15), borderSide: const BorderSide(color: Colors.indigo, width: 1.5)),
      contentPadding: const EdgeInsets.symmetric(horizontal: 16, vertical: 14),
    );
  }

