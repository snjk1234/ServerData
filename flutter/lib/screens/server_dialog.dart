                    ),
                    child: const Text('إلغاء', style: TextStyle(color: Colors.grey, fontWeight: FontWeight.bold, fontSize: 16)),
                  ),
                  const SizedBox(width: 12),
                  ElevatedButton(
                    onPressed: _saveData,
                    style: ElevatedButton.styleFrom(
                      backgroundColor: Colors.indigo,
                      foregroundColor: Colors.white,
                      padding: const EdgeInsets.symmetric(horizontal: 32, vertical: 12),
                      elevation: 2,
                      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
                    ),
                    child: Text(isEditing ? 'حفظ التعديلات' : 'إضافة الفرع', style: const TextStyle(fontWeight: FontWeight.bold, fontSize: 16, letterSpacing: 0.5)),
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }

  Future<void> _saveData() async {
    final isEditing = widget.serverToEdit != null;

    if (branchNoController.text.isEmpty ||
        nameArController.text.isEmpty ||
        nameEnController.text.isEmpty ||
        usernameController.text.isEmpty ||
        (!isEditing && passwordController.text.isEmpty)) {
      _showError('يرجى إدخال الحقول المطلوبة الأساسية');
      return;
    }

    if (selectedCategory != 'الإدارة' && nameEnController.text.length < 4) {
      _showError('اسم الفرع بالإنجليزية يجب أن يكون 4 أحرف على الأقل');
      return;
    }

    if (selectedCategory != 'الإدارة' && printerA4Controller.text.isNotEmpty && printerA4Controller.text.length != 12) {
      _showPrinterErrorDialog();
      return;
    }

    try {
      final response = await Supabase.instance.client.from('server_data').select('id, رقم_الفرع, اسم_الفرع_ar, طابعة_a4, طابعة_فواتير');
      final freshServers = (response as List).map((s) => ServerData.fromMap(s)).toList();
      
      if (selectedCategory != 'الإدارة') {
        final duplicate = freshServers.firstWhere(
          (s) => s.id != (widget.serverToEdit?.id ?? -1) && 
                 ((s.printerA4 == printerA4Controller.text && printerA4Controller.text.isNotEmpty) ||
                  (s.printerBill == printerBillController.text && printerBillController.text.isNotEmpty)),
          orElse: () => ServerData(id: 0, branchNo: '', category: '', branchNameAr: '', branchNameEn: '', username: '', encryptedPassword: '', status: '', createdAt: DateTime.now(), serialNumber: 0),
        );

        if (duplicate.id != 0) {
          if (mounted) {
            showDialog(
              context: context,
              builder: (context) => AlertDialog(
                shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(24)),
                title: const Row(
                  children: [
                    Icon(Icons.info_outline_rounded, color: Colors.amber, size: 28),
                    SizedBox(width: 12),
                    Text('تعارض في الطابعة', style: TextStyle(fontWeight: FontWeight.bold, fontSize: 20)),
                  ],
                ),
                content: Text(
                  'اسم الطابعة مستخدم مسبقاً في:\n(${duplicate.branchNo}) - ${duplicate.branchNameAr}',
                  style: const TextStyle(fontSize: 15, color: Colors.black87),
                ),
                actions: [
                  TextButton(
                    onPressed: () => Navigator.pop(context),
                    child: const Text('موافق', style: TextStyle(color: Colors.indigo, fontWeight: FontWeight.bold)),
                  ),
                ],
              ),
            );
          }
          return;
        }
      }

      String? encryptedPass;
      if (passwordController.text.isNotEmpty) {
        encryptedPass = encryptAESCryptoJS(passwordController.text, adminKey);
      } else {
        encryptedPass = widget.serverToEdit?.encryptedPassword;
      }

      final reqFields = _getRequiredFields(selectedCategory);
      final bool isStreetCityEnabled = reqFields['street']! || reqFields['city']!;
      final bool isTaxNoEnabled = reqFields['taxNo']!;
      final bool isSerialPrintersHidden = selectedCategory == 'الإدارة';

      final dataPayload = {
        'رقم_الفرع': branchNoController.text,
        'تصنيف_الفرع': selectedCategory,
        'اسم_الفرع_ar': nameArController.text,
        'اسم_الفرع_en': nameEnController.text.toUpperCase(),
        'اسم_اليوزر': usernameController.text,
        'باسوورد': encryptedPass,
        'حالة_اليوزر': selectedStatus,
        'serial_number': isSerialPrintersHidden ? null : int.tryParse(serialController.text),
        'المنطقة': selectedRegion,
        'اسم_الشارع': (!isStreetCityEnabled || streetController.text.isEmpty) ? null : streetController.text,
        'اسم_المدينة': (!isStreetCityEnabled || cityController.text.isEmpty) ? null : cityController.text,
        'الرقم_الضريبي': (!isTaxNoEnabled || taxNoController.text.isEmpty) ? null : taxNoController.text,
        'طابعة_a4': (isSerialPrintersHidden || printerA4Controller.text.isEmpty) ? null : printerA4Controller.text,
        'طابعة_فواتير': (isSerialPrintersHidden || printerBillController.text.isEmpty) ? null : printerBillController.text,
      };

      if (!isEditing) {
        await Supabase.instance.client.from('server_data').insert(dataPayload);
      } else {
        await Supabase.instance.client.from('server_data').update(dataPayload).eq('id', widget.serverToEdit!.id);
      }

      if (mounted) {
        Navigator.pop(context); 
        widget.onSaved();
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Row(
              children: [
                const Icon(Icons.check_circle_rounded, color: Colors.white),
                const SizedBox(width: 12),
                Text(isEditing ? 'تم تحديث بيانات الفرع بنجاح' : 'تم إضافة الفرع بنجاح', style: const TextStyle(fontWeight: FontWeight.bold, fontSize: 16)),
              ],
            ),
            backgroundColor: Colors.teal.shade600,
            behavior: SnackBarBehavior.floating,
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
            margin: const EdgeInsets.all(16),
            padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 14),
          ),
        );
      }
    } catch (e) {
      if (mounted) {
        _showError('حدث خطأ أثناء الحفظ: $e');
      }
    }
  }

  void _showError(String msg) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Row(
          children: [
            const Icon(Icons.error_outline_rounded, color: Colors.white),
            const SizedBox(width: 12),
            Expanded(child: Text(msg, style: const TextStyle(fontWeight: FontWeight.bold))),
          ],
        ),
        backgroundColor: Colors.redAccent,
        behavior: SnackBarBehavior.floating,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
        margin: const EdgeInsets.all(16),
      ),
    );
  }

  void _showPrinterErrorDialog() {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(24)),
        title: const Row(
          children: [
            Icon(Icons.warning_amber_rounded, color: Colors.amber, size: 28),
            SizedBox(width: 12),
            Text('خطأ في تنسيق الطابعة', style: TextStyle(fontWeight: FontWeight.bold, fontSize: 18)),
          ],
        ),
        content: const Text(
          'يجب أن يتكون اسم طابعة A4 من 4 أحرف للفرع متبوعة بـ BIGPRIN1 (الإجمالي 12 حرف).',
          style: TextStyle(fontSize: 15, height: 1.5),
        ),
        actions: [
          ElevatedButton(
            onPressed: () => Navigator.pop(context),
            style: ElevatedButton.styleFrom(
              backgroundColor: Colors.amber.shade700,
              foregroundColor: Colors.white,
              shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
              padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 12),
            ),
            child: const Text('فهمت', style: TextStyle(fontWeight: FontWeight.bold)),
          ),
        ],
      ),
    );
  }
}
