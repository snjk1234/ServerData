        child: Column(
          children: [
            Container(
              padding: const EdgeInsets.all(20),
              decoration: const BoxDecoration(
                gradient: LinearGradient(colors: [Colors.indigo, Colors.blueAccent], begin: Alignment.topLeft, end: Alignment.bottomRight),
                borderRadius: BorderRadius.only(topLeft: Radius.circular(24), topRight: Radius.circular(24)),
              ),
              child: Row(
                children: [
                  Icon(isEditing ? Icons.edit_note_rounded : Icons.add_circle_outline_rounded, color: Colors.white, size: 28),
                  const SizedBox(width: 12),
                  Expanded(
                    child: Text(
                      isEditing ? 'تعديل بيانات السيرفر (${widget.serverToEdit!.branchNo})' : 'إضافة سيرفر جديد',
                      style: const TextStyle(color: Colors.white, fontSize: 20, fontWeight: FontWeight.bold),
                    ),
                  ),
                  IconButton(
                    icon: const Icon(Icons.close, color: Colors.white),
                    onPressed: () => Navigator.pop(context),
                    padding: EdgeInsets.zero,
                    constraints: const BoxConstraints(),
                  ),
                ],
              ),
            ),
            Expanded(
              child: SingleChildScrollView(
                padding: const EdgeInsets.all(24),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.stretch,
                  children: [
                    DropdownButtonFormField<String>(
                      value: selectedCategory,
                      decoration: _customDecoration(label: 'تصنيف الفرع', icon: Icons.category_rounded),
                      items: ['فلورينا', 'فرنشايز', 'جملة', 'موزع معتمد', 'اسكتشر', 'فيلانتو', 'الإدارة', 'كمبيوتر وملحقات']
                          .map((c) => DropdownMenuItem(value: c, child: Text(c, style: const TextStyle(fontWeight: FontWeight.w600))))
                          .toList(),
                      onChanged: (val) {
                        setState(() {
                          selectedCategory = val!;
                          if (!isEditing) {
                            branchNoEdited = false;
                            updateAutoGeneratedFields(selectedCategory);
                          }
                        });
                      },
                    ),
                    const SizedBox(height: 16),
                    TextField(
                      controller: branchNoController,
                      decoration: _customDecoration(label: 'رقم الفرع', icon: Icons.numbers_rounded),
                      onChanged: (v) => branchNoEdited = true,
                    ),
                    const SizedBox(height: 16),
                    TextField(
                      controller: nameArController,
                      decoration: _customDecoration(label: 'اسم الفرع بالعربي', icon: Icons.store_mall_directory_rounded),
                    ),
                    const SizedBox(height: 16),
                    TextField(
                      controller: nameEnController,
                      decoration: _customDecoration(label: 'اسم الفرع بالإنجليزي', icon: Icons.store_mall_directory_outlined),
                      textCapitalization: TextCapitalization.characters,
                    ),
                    const SizedBox(height: 16),
                    TextField(
                      controller: usernameController,
                      decoration: _customDecoration(label: 'اسم اليوزر', icon: Icons.person_rounded),
                    ),
                    const SizedBox(height: 16),
                    TextField(
                      controller: passwordController,
                      obscureText: obscurePassword,
                      decoration: _customDecoration(
                        label: isEditing ? 'إدخال كلمة سر جديدة (اتركه فارغاً للاحتفاظ بالقديم)' : 'كلمة المرور',
                        icon: Icons.lock_rounded,
                        suffix: IconButton(
                          icon: Icon(obscurePassword ? Icons.visibility_off_rounded : Icons.visibility_rounded, color: Colors.indigo),
                          onPressed: () => setState(() => obscurePassword = !obscurePassword),
                        ),
                      ),
                    ),
                    if (selectedCategory != 'الإدارة') ...[
                      const SizedBox(height: 16),
                      TextField(
                        controller: serialController,
                        keyboardType: TextInputType.number,
                        decoration: _customDecoration(label: 'التسلسل (Serial Number)', icon: Icons.tag_rounded),
                      ),
                    ],
                    const SizedBox(height: 16),
                    Row(
                      children: [
                        Expanded(
                          child: DropdownButtonFormField<String>(
                            value: selectedStatus,
                            decoration: _customDecoration(label: 'حالة الفرع', icon: Icons.info_rounded),
                            items: ['يعمل', 'الافتتاح قريبا', 'مغلق مؤقتا', 'مغلق نهائيا']
                                .map((s) => DropdownMenuItem(value: s, child: Text(s)))
                                .toList(),
                            onChanged: (val) => setState(() => selectedStatus = val!),
                          ),
                        ),
                        const SizedBox(width: 16),
                        Expanded(
                          child: DropdownButtonFormField<String>(
                            value: selectedRegion,
                            decoration: _customDecoration(label: 'المنطقة', icon: Icons.map_rounded),
                            items: _regions.map((r) => DropdownMenuItem(value: r, child: Text(r, overflow: TextOverflow.ellipsis))).toList(),
                            onChanged: (val) => setState(() => selectedRegion = val),
                          ),
                        ),
                      ],
                    ),
                    const SizedBox(height: 24),
                    const Row(
                      children: [
                        Icon(Icons.more_horiz_rounded, color: Colors.indigo, size: 20),
                        SizedBox(width: 8),
                        Text('بيانات إضافية', style: TextStyle(fontWeight: FontWeight.bold, fontSize: 16, color: Colors.indigo)),
                      ],
                    ),
                    const Divider(height: 24, thickness: 1),
                    TextField(
                      controller: streetController,
                      decoration: _customDecoration(
                        label: 'اسم الشارع',
                        icon: Icons.add_road_rounded,
                        suffix: reqFields['street']! ? const Icon(Icons.star, color: Colors.redAccent, size: 12) : null,
                      ),
                      enabled: reqFields['street']! || reqFields['city']!,
                    ),
                    const SizedBox(height: 16),
                    Row(
                      children: [
                        Expanded(
                          child: TextField(
                            controller: cityController,
                            decoration: _customDecoration(
                              label: 'اسم المدينة',
                              icon: Icons.location_city_rounded,
                              suffix: reqFields['city']! ? const Icon(Icons.star, color: Colors.redAccent, size: 12) : null,
                            ),
                            enabled: reqFields['street']! || reqFields['city']!,
                          ),
                        ),
                        const SizedBox(width: 16),
                        Expanded(
                          child: TextField(
                            controller: taxNoController,
                            decoration: _customDecoration(
                              label: 'الرقم الضريبي',
                              icon: Icons.receipt_long_rounded,
                              suffix: reqFields['taxNo']! ? const Icon(Icons.star, color: Colors.redAccent, size: 12) : null,
                            ),
                            enabled: reqFields['taxNo']!,
                          ),
                        ),
                      ],
                    ),
                    if (selectedCategory != 'الإدارة') ...[
